	                <br>2. Peas</br>
	                <br>  Peas are a trouble free crop that enjoy cooler weather. Sow them directly into the ground from March to June and look forward to the incredible sweet flavour of fresh picked peas from June to August. All they need is support for their stems - simply erect some chicken wire or netting between supports at each end of the row. You'll be amazed at how good fresh peas taste - and the more that you pick them, the more they produce!</br>
