	                <br>4. Onion and Garlic</br>
	                <br>  Onions and garlic are virtually maintenance-free crops, and are such easy vegetables to grow. Simply plant onion bulbs and individual garlic cloves on well drained soil in spring or autumn - then leave them to it! In late summer when the foliage yellows and dies back, you can lift them and dry them in the sun before storing them.</br>

