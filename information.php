	                 <h4><br>1. Potatoes </br>
	                <br>A fun crop to grow Plant potatoes during late February and March in potato bags that are only part filled with compost. When the green shoots begin to appear above the soil, simply cover them with more compost. Repeat until the bag is full, and then you only need remember to water them! The real fun comes at the end of the season, 10 to 20 weeks later when the foliage starts to yellow and die back. Tip the bag out and rummage around in the soil to collect up your own home grown potatoes. Potatoes are such easy vegetables to grow at home!</br>
	                
