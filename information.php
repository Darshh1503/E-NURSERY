	                <br>Water is life and just like us, plants also need adequate water to survive. But the quantity of water you are giving is important. Too little or too much water may affect the health of the plant. So, you must be aware of the water requirements.Try to water the roots only and take up techniques to limit the accumulation of moisture on a plant’s foliage and hence avoid overhead sprinkling.</br>
