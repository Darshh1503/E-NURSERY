	                <h4><br>1.Examine the plants carefully before buying</br>
	                <br>Sometimes a plant which looks healthy from the top may have a rotten root which will kill the whole plant in no time. Make sure that the root is white and firm and spaced all over the root ball and avoid the dark ones.</br>
	                <br>2. Use a yard paste that is fully composted</br>
	                <br> While you are using a yard paste, be sure that is fully composted.So, you must be very careful and inspect properly before using any such waste in your plants especially on the plants that are very sensitive.</br>
	                <br>3. Keep it clean and tidy</br>
	                <br>Keep your garden clean all through the year especially during the fall.since during falls your garden gets most dirty, you must be more careful that time. Remove all the dead leaves properly as if you keep them, then it will actually infect the fresh leaves that will come out in spring and slowly infect your full garden.</br>
