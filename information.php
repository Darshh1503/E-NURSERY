	               </div>
	                <br>4. Use fertilizers carefully</br>
	                <br>The best is to go organic and avoid fertilizers when possible. Use natural fertilizing agents like compost etc. But if you choose to use fertilizers, then you better be careful. Fertilizers are definitely good for your plants but sometimes, if you are not careful, you may go wrong there as well.</br>
	               
	                <br>5. Prune the damaged limbs properly</br>
	                <br>It is very important that your prune and trim the plants properly and at the right time. This is mainly due to the facts that the limbs that are wounded may actually give a way to occurrence of diseases.</br>
	                <br>6. Arrange and plant the trees properly</br>
	                <br>If you want to do proper gardening then you must arrange and place your trees properly as per their sunlight requirements and it is certainly one of the easiest ways to maintain your garden and keep it healthy.</br>
