	                <br>5. Tomatoes</br>
	                <br>  Tomato plants are so quick that you can almost watch them grow, so they are the ideal easy vegetable for kids to cultivate. Choose a bush variety like 'Romello' that can be planted in hanging baskets and window boxes. Bush varieties don't require training or side-shooting, so you only need to feed and water them before the fruit starts to pour from the plant</br>

	                
	                

						 
